import { promises as fs } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const pluginsRoot = path.join(repoRoot, 'plugins')
const publicPluginsRoot = path.join(repoRoot, 'frontend', 'public', 'plugins')

async function syncPluginRemoteEntries() {
  let entries = []
  try {
    entries = await fs.readdir(pluginsRoot, { withFileTypes: true })
  } catch {
    return
  }

  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue
    }

    const pluginKey = entry.name
    const source = path.join(pluginsRoot, pluginKey, 'frontend', 'src', 'remoteEntry.js')
    const targetDir = path.join(publicPluginsRoot, pluginKey)
    const target = path.join(targetDir, 'remoteEntry.js')

    try {
      const sourceText = await fs.readFile(source, 'utf8')
      const finalText = [
        '// Auto-generated from plugins/<plugin>/frontend/src/remoteEntry.js',
        '// Do not edit this file directly.',
        '',
        sourceText
      ].join('\n')
      await fs.mkdir(targetDir, { recursive: true })
      await fs.writeFile(target, finalText, 'utf8')
    } catch {
      // Ignore plugins without remote entry source.
    }
  }
}

await syncPluginRemoteEntries()
